public class Selective {
    public boolean boy;
    public boolean girl;
    public boolean less;
    public boolean gay;
    public boolean old;
    public int toOld;
    public int fromOld;

    public Selective() {
    }

    public Selective(boolean boy, boolean girl, boolean less, boolean gay, boolean old, int toOld, int fromOld) {
        this.boy = boy;
        this.girl = girl;
        this.less = less;
        this.gay = gay;
        this.old = old;
        this.toOld = toOld;
        this.fromOld = fromOld;
    }
}
